package teams;

public class Personnel {
    private int QB;
    private int HB;
    private int WR;
    private int TE;
    private int OL;
    private int DL;
    private int LB;
    private int CB;
    private int S;
    private int P;
    private int K;

    public int getQB() {
        return QB;
    }

    public void setQB(int QB) {
        this.QB = QB;
    }

    public int getHB() {
        return HB;
    }

    public void setHB(int HB) {
        this.HB = HB;
    }

    public int getWR() {
        return WR;
    }

    public void setWR(int WR) {
        this.WR = WR;
    }

    public int getTE() {
        return TE;
    }

    public void setTE(int TE) {
        this.TE = TE;
    }

    public int getOL() {
        return OL;
    }

    public void setOL(int OL) {
        this.OL = OL;
    }

    public int getDL() {
        return DL;
    }

    public void setDL(int DL) {
        this.DL = DL;
    }

    public int getLB() {
        return LB;
    }

    public void setLB(int LB) {
        this.LB = LB;
    }

    public int getCB() {
        return CB;
    }

    public void setCB(int CB) {
        this.CB = CB;
    }

    public int getS() {
        return S;
    }

    public void setS(int s) {
        S = s;
    }

    public int getP() {
        return P;
    }

    public void setP(int p) {
        P = p;
    }

    public int getK() {
        return K;
    }

    public void setK(int k) {
        K = k;
    }
}
